public class R010ProductItemModel {
    private String productCode;
    private String productName;
    private String size;
    private String material;
    private String print;
    private String unitPrice;
    private String lot;
    private String woodenCode;
    private String woodenExpiredDate;
    private String note;

    public String getProductCode() {
        return productCode;
    }

    public void setProductCode(String productCode) {
        this.productCode = productCode;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getPrint() {
        return print;
    }

    public void setPrint(String print) {
        this.print = print;
    }

    public String getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(String unitPrice) {
        this.unitPrice = unitPrice;
    }

    public String getLot() {
        return lot;
    }

    public void setLot(String lot) {
        this.lot = lot;
    }

    public String getWoodenCode() {
        return woodenCode;
    }

    public void setWoodenCode(String woodenCode) {
        this.woodenCode = woodenCode;
    }

    public String getWoodenExpiredDate() {
        return woodenExpiredDate;
    }

    public void setWoodenExpiredDate(String woodenExpiredDate) {
        this.woodenExpiredDate = woodenExpiredDate;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
